from waitress import serve
import falcon

# ================================================== #
#                     FUNCTIONS                      #
# ================================================== #

def start_server(socket="", port=8080):
    app = falcon.App()
    if not socket:
        serve(app, port=port)
    else:
        serve(app, unix_socket=socket)


